/** Curated groups in the order they appear in the sidebar. Components
 *  and Icons are autogenerated from `content/docs/{components,icons}/`
 *  and live as separate sidebar separators outside this list. */
export const DOCS_GROUPS: DocsGroup[] = [
  GET_STARTED,
  FOUNDATIONS,
  AI_DISTRIBUTION,
];

function toSidebarItems(entries: SidebarEntry[]): PageTree.Item[] {
  return entries.map((entry) => ({
    type: 'page',
    name: entry.label,
    url: entry.url,
  }));
}

const COMPONENTS_INDEX_URL = '/components';
const ICONS_INDEX_URL = '/icons';

function buildComponentsList(componentsTree: PageTree.Root): PageTree.Item[] {
  const all = flattenRootPages(componentsTree).filter(
    (page) => page.url !== COMPONENTS_INDEX_URL
  );
  return all.sort((a, b) =>
    toLabel(a.name, '').localeCompare(toLabel(b.name, ''))
  );
}

function buildIconsList(iconsTree: PageTree.Root): PageTree.Item[] {
  // Order is preserved from `content/icons/meta.json` (Plex first as
  // the brand-bundled set, then external libraries) — unlike
  // components, alphabetic order here would bury Plex below Hugeicons.
  return flattenRootPages(iconsTree).filter(
    (page) => page.url !== ICONS_INDEX_URL
  );
}

/**
 * Reshapes three independent fumadocs source trees (docs, components,
 * icons) into a single shadcn/ui-style sidebar with five labeled
 * groups. Each source contributes its own slice:
 *
 *   ─ Get Started ─       hardcoded URLs in DOCS_GROUPS (under /docs)
 *   ─ Foundations ─       hardcoded URLs in DOCS_GROUPS (under /docs)
 *   ─ AI & Distribution ─ hardcoded URLs in DOCS_GROUPS (under /docs)
 *   ─ Icons ─             flattened from iconsTree (/icons/*)
 *   ─ Components ─        flattened from componentsTree (/components/*)
 *
 * The merged tree is the SAME for every page across all three sources —
 * a user reading /components/button still sees Get Started + Foundations
 * + AI & Distribution at the top of the sidebar.
 */
function buildMergedTree(trees: {
  docs: PageTree.Root;
  components: PageTree.Root;
  icons: PageTree.Root;
}): PageTree.Root {
  const componentsList = buildComponentsList(trees.components);
  const iconsList = buildIconsList(trees.icons);

  const children: PageTree.Node[] = [];

  for (const group of DOCS_GROUPS) {
    children.push({ type: 'separator', name: group.label });
    children.push(...toSidebarItems(group.entries));
  }

  if (iconsList.length > 0) {
    children.push({ type: 'separator', name: 'Icons' });
    children.push(...iconsList);
  }

  if (componentsList.length > 0) {
    children.push({ type: 'separator', name: 'Components' });
    children.push(...componentsList);
  }

  return {
    name: trees.docs.name,
    children,
  };
}

/**
 * Top-nav sections (label + href + match rules) — drives DocsGlobalNav
 * highlighting. Three peer entries: Docs, Components, Icons. Bridge
 * and Blog are added separately in DocsGlobalNav itself (they're not
 * docs-tree-derived).
 */
function buildSections(): DocsSectionNavItem[] {
  return [
    {
      slug: 'docs',
      label: 'Docs',
      href: '/docs',
      // Section slugs that should highlight "Docs" in the top nav. After
      // the URL split, /components and /icons live at the top level —
      // they shouldn't highlight Docs.
      matchSlugs: [...DOCS_SECTION_SLUGS],
    },
    {
      slug: 'components',
      label: 'Components',
      href: '/components',
      matchSlugs: ['components'],
    },
    {
      slug: 'icons',
      label: 'Icons',
      href: '/icons',
      matchSlugs: ['icons'],
    },
  ].sort((a, b) => {
    const orderA = navOrder(a.slug);
    const orderB = navOrder(b.slug);
    if (orderA !== orderB) return orderA - orderB;
    return a.label.localeCompare(b.label);
  });
}

export function buildDocsTreeNavigation(trees: {
  docs: PageTree.Root;
  components: PageTree.Root;
  icons: PageTree.Root;
}): {
  tree: PageTree.Root;
  sections: DocsSectionNavItem[];
} {
  // Each input tree is read-only fumadocs output; deep-clone before we
  // reshape so we don't mutate the live source.pageTree references.
  const cloned = {
    docs: cloneRoot(trees.docs),
    components: cloneRoot(trees.components),
    icons: cloneRoot(trees.icons),
  };

  return {
    tree: buildMergedTree(cloned),
    sections: buildSections(),
  };
}
