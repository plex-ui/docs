import type * as PageTree from 'fumadocs-core/page-tree';
import type { ReactNode } from 'react';

export interface DocsSectionNavItem {
  slug: string;
  label: string;
  href: string;
  matchSlugs?: string[];
}

const DOCS_SECTION_SLUGS = [
  'foundations',
  'concepts',
  'hooks',
  'transitions',
  'installation',
  'skills',
  'ai-setup',
  'mcp',
  'registry',
  'changelog',
] as const;

const NAV_ORDER = ['docs', 'components'] as const;

interface SectionFolder {
  slug: string;
  folder: PageTree.Folder;
  firstUrl: string;
}

function isFolder(node: PageTree.Node): node is PageTree.Folder {
  return node.type === 'folder';
}

function cloneNode(node: PageTree.Node): PageTree.Node {
  if (node.type === 'folder') {
    return {
      ...node,
      index: node.index ? { ...node.index } : undefined,
      children: node.children.map(cloneNode),
    };
  }

  if (node.type === 'page') {
    return { ...node };
  }

  return { ...node };
}

function cloneRoot(root: PageTree.Root): PageTree.Root {
  return {
    ...root,
    children: root.children.map(cloneNode),
    fallback: root.fallback ? cloneRoot(root.fallback) : undefined,
  };
}

function toLabel(value: ReactNode, fallback: string): string {
  if (typeof value === 'string') return value;
  if (typeof value === 'number') return String(value);
  return fallback;
}

function sectionSlugFromUrl(url: string): string | null {
  if (!url.startsWith('/')) return null;

  const parts = url.split('/').filter(Boolean);
  const docsIndex = parts.indexOf('docs');
  if (docsIndex === -1) return null;

  return parts[docsIndex + 1] ?? null;
}

function firstPageUrl(nodes: PageTree.Node[]): string | null {
  for (const node of nodes) {
    if (node.type === 'page') return node.url;
    if (node.type === 'folder') {
      if (node.index) return node.index.url;
      const nested = firstPageUrl(node.children);
      if (nested) return nested;
    }
  }

  return null;
}

function flattenFolderPages(folder: PageTree.Folder): PageTree.Item[] {
  const output: PageTree.Item[] = [];
  const seen = new Set<string>();

  const addItem = (item: PageTree.Item) => {
    if (seen.has(item.url)) return;
    seen.add(item.url);
    output.push({ ...item });
  };

  const walk = (nodes: PageTree.Node[]) => {
    for (const node of nodes) {
      if (node.type === 'page') addItem(node);
      if (node.type === 'folder') {
        if (node.index) addItem(node.index);
        walk(node.children);
      }
    }
  };

  if (folder.index) addItem(folder.index);
  walk(folder.children);

  return output;
}

/** Same as `flattenFolderPages` but operates on a `PageTree.Root`
 *  (no `index` property at root). Used for `componentsSource.pageTree`
 *  and `iconsSource.pageTree` where the Root IS the section folder. */
function flattenRootPages(root: PageTree.Root): PageTree.Item[] {
  const output: PageTree.Item[] = [];
  const seen = new Set<string>();

  const addItem = (item: PageTree.Item) => {
    if (seen.has(item.url)) return;
    seen.add(item.url);
    output.push({ ...item });
  };

  const walk = (nodes: PageTree.Node[]) => {
    for (const node of nodes) {
      if (node.type === 'page') addItem(node);
      if (node.type === 'folder') {
        if (node.index) addItem(node.index);
        walk(node.children);
      }
    }
  };

  walk(root.children);

  return output;
}

function getSectionFolders(root: PageTree.Root): SectionFolder[] {
  const folders: SectionFolder[] = [];

  for (const node of root.children) {
    if (!isFolder(node)) continue;

    const firstUrl = node.index?.url ?? firstPageUrl(node.children);
    const slug = firstUrl ? sectionSlugFromUrl(firstUrl) : null;
    if (!slug || !firstUrl) continue;

    folders.push({ slug, folder: node, firstUrl });
  }

  return folders;
}

function navOrder(slug: string): number {
  const index = NAV_ORDER.indexOf(slug as (typeof NAV_ORDER)[number]);
  return index === -1 ? Number.MAX_SAFE_INTEGER : index;
}

/**
 * Hand-curated sidebar groups. URLs are hardcoded so the list stays
 * stable regardless of how Fumadocs flattens `meta.json`-rooted
 * folders inside `source.pageTree`.
 *
 * Layout (top to bottom of the sidebar):
 *
 *   ─ Get Started ─       3 entry points
 *   ─ Foundations ─       design tokens, colors, typography + dark mode + responsive
 *   ─ AI & Distribution ─ everything related to integrating Plex UI into AI tooling
 *                         and shipping Plex UI to consumers
 *   ─ API ─               runtime utilities (hooks, transitions)
 *   ─ Icons ─             autogenerated by buildIconsList
 *   ─ Components ─        autogenerated by buildComponentsList
 *
 * Folder index pages (/docs/foundations, /docs/concepts) are omitted on
 * purpose — their job was to list children, and the sidebar already
 * does that. Direct links still 200, just no sidebar entry.
 */

export type SidebarEntry = { label: string; url: string };

export type DocsGroup = {
  /** Stable identifier — used as a key by consumers picking specific groups. */
  id: string;
  /** Title shown above the entries (in the sidebar separator and in
   *  any presentational use like the /docs intro cards). */
  label: string;
  /** Short tagline describing what the group covers — used by the
   *  /docs intro cards under the title. Not shown in the sidebar. */
  description: string;
  entries: SidebarEntry[];
};

